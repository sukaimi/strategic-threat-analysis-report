function readRecentAnalyses(hours, base) {
  hours = hours || 24;
  const root = base || VAULT;
  const dir = path.join(root, 'analyses');
  if (!fs.existsSync(dir)) return [];

  const cutoff = Date.now() - hours * 3600 * 1000;
  const files = fs
    .readdirSync(dir)
    .filter((f) => f.endsWith('.md'))
    .sort()
    .reverse();

  const results = [];
  for (const f of files) {
    if (results.length >= 10) break;
    const fpath = path.join(dir, f);
    const stat = fs.statSync(fpath);
    if (stat.mtimeMs < cutoff) continue;
    const raw = fs.readFileSync(fpath, 'utf8');
    const parsed = parseFrontmatter(raw);
    results.push({ file: f, ...parsed });
  }
  return results;
}

function readOpenIncidents(base) {
  const root = base || VAULT;
  const dir = path.join(root, 'incidents');
  if (!fs.existsSync(dir)) return [];

  const files = fs.readdirSync(dir).filter((f) => f.endsWith('.md'));
  const results = [];
  for (const f of files) {
    const raw = fs.readFileSync(path.join(dir, f), 'utf8');
    const parsed = parseFrontmatter(raw);
    if (parsed.meta.status === 'OPEN') {
      results.push({ file: f, ...parsed });
    }
  }
  return results;
}

function readVesselHistory(mmsi, base) {
  const root = base || VAULT;
  const fpath = path.join(root, 'vessels', `MMSI-${mmsi}.md`);
  if (!fs.existsSync(fpath)) return null;
  const raw = fs.readFileSync(fpath, 'utf8');
  return parseFrontmatter(raw);
}

function readPatterns(base) {
  const root = base || VAULT;
  const dir = path.join(root, 'patterns');
  if (!fs.existsSync(dir)) return [];

  const files = fs.readdirSync(dir).filter((f) => f.endsWith('.md'));
  return files.map((f) => {
    const raw = fs.readFileSync(path.join(dir, f), 'utf8');
    return { file: f, ...parseFrontmatter(raw) };
  });
}

function getVaultContext(maxTokens, base) {
  maxTokens = maxTokens || 3000;
  const root = base || VAULT;
  const parts = [];
  let est = 0;

  // Rough token estimate: ~4 chars per token
  const charBudget = maxTokens * 4;

  // Last 12 analyses
  const analyses = readRecentAnalyses(168, root).slice(0, 12);
  if (analyses.length > 0) {
    parts.push('## Recent Analyses');
    for (const a of analyses) {
      const summary =
        a.body.length > 300 ? a.body.slice(0, 300) + '...' : a.body;
      const entry = `### ${a.file}\nScore: ${a.meta.composite_score || '?'} | Level: ${a.meta.threat_level || '?'}\n${summary}\n`;
      est += entry.length;
      if (est > charBudget) break;
      parts.push(entry);
    }
  }

  // Open incidents
  const incidents = readOpenIncidents(root);
  if (incidents.length > 0 && est < charBudget) {
    parts.push('## Open Incidents');
    for (const inc of incidents) {
      const entry = `- ${inc.meta.id || inc.file}: ${inc.meta.severity || '?'} (${inc.meta.entity_mmsi || 'N/A'})\n`;
      est += entry.length;
      if (est > charBudget) break;
      parts.push(entry);
    }
  }

  // Vessel notes (just list them)
  const vdir = path.join(root, 'vessels');
  if (fs.existsSync(vdir) && est < charBudget) {
    const vfiles = fs.readdirSync(vdir).filter((f) => f.endsWith('.md'));
    if (vfiles.length > 0) {
      parts.push('## Tracked Vessels');
      for (const f of vfiles.slice(0, 20)) {
        const entry = `- ${f.replace('.md', '')}\n`;
        est += entry.length;
        if (est > charBudget) break;
        parts.push(entry);
      }
    }
  }

  return parts.join('\n');
}

