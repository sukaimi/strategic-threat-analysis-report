function writeAnalysis(data, base) {
  const root = base || VAULT;
  ensureDirs(root);
  const now = data._date || new Date();
  const score = data.composite_score != null ? data.composite_score : 0;
  const level = data.threat_level || threatLabel(score);
  const fname = fileDate(now) + '.md';
  const fpath = path.join(root, 'analyses', fname);

  const content = [
    '---',
    `timestamp: ${localISO(now)}`,
    `composite_score: ${score}`,
    `threat_level: ${level}`,
    '---',
    `# Analysis — ${friendlyDate(now)}`,
    '',
    `## Threat Score: ${score}/100 (${level})`,
    '',
    '## Tactical Brief',
    data.tactical_brief || '_No brief provided._',
    '',
    '## Key Findings',
    formatList(data.key_findings) || formatList(data.findings) || '_None._',
    '',
    '## Priority Actions',
    formatList(data.priority_actions) || formatList(data.actions) || '_None._',
    '',
    '## Vessel Anomalies',
    formatAnomalies(data.vessel_anomalies) || formatList(data.anomalies) || '_None._',
    '',
    '## Forecast',
    `**6h:** ${data.forecast_6h || '_Unavailable._'}`,
    `**24h:** ${data.forecast_24h || '_Unavailable._'}`,
    '',
    '---',
    '*Generated by SPECTRE AI Engine*',
    '',
  ].join('\n');

  fs.writeFileSync(fpath, content, 'utf8');
  return fpath;
}

function getNextIncidentId(base) {
  const root = base || VAULT;
  const dir = path.join(root, 'incidents');
  ensureDirs(root);
  const year = new Date().getFullYear();
  const prefix = `INC-${year}-`;

  let max = 0;
  if (fs.existsSync(dir)) {
    const files = fs.readdirSync(dir).filter((f) => f.startsWith(prefix));
    for (const f of files) {
      const m = f.match(/INC-\d{4}-(\d{3})/);
      if (m) {
        const n = parseInt(m[1], 10);
        if (n > max) max = n;
      }
    }
  }
  return `${prefix}${String(max + 1).padStart(3, '0')}`;
}

function writeIncident(alert, base) {
  const root = base || VAULT;
  ensureDirs(root);
  const now = alert._date || new Date();
  const id = alert.id || getNextIncidentId(root);
  const severity = alert.severity || 'MEDIUM';
  const status = alert.status || 'OPEN';
  const title = alert.title || 'Untitled Incident';
  const description = alert.description || '_No details._';
  const mmsi = alert.entity_mmsi || '';
  const fname = id + '.md';
  const fpath = path.join(root, 'incidents', fname);

  const content = [
    '---',
    `id: ${id}`,
    `severity: ${severity}`,
    `status: ${status}`,
    `created: ${localISO(now)}`,
    `entity_mmsi: ${mmsi}`,
    '---',
    `# ${id} — ${title}`,
    '',
    '## Details',
    description,
    '',
    '## Timeline',
    `- ${friendlyDate(now)} — Incident created`,
    '',
  ].join('\n');

  fs.writeFileSync(fpath, content, 'utf8');
  return fpath;
}

function writeVesselNote(mmsi, data, base) {
  const root = base || VAULT;
  ensureDirs(root);
  const fname = `MMSI-${mmsi}.md`;
  const fpath = path.join(root, 'vessels', fname);
  const now = data._date || new Date();

  if (fs.existsSync(fpath)) {
    const entry = `\n- ${friendlyDate(now)} — ${data.note || 'Update'}\n`;
    fs.appendFileSync(fpath, entry, 'utf8');
  } else {
    const content = [
      '---',
      `mmsi: ${mmsi}`,
      `vessel_name: ${data.vessel_name || 'UNKNOWN'}`,
      `created: ${localISO(now)}`,
      '---',
      `# Vessel ${data.vessel_name || mmsi}`,
      '',
      '## Observations',
      `- ${friendlyDate(now)} — ${data.note || 'Initial observation'}`,
      '',
    ].join('\n');
    fs.writeFileSync(fpath, content, 'utf8');
  }
  return fpath;
}

