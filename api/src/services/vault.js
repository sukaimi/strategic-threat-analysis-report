function updateIndex(base) {
  const root = base || VAULT;
  ensureDirs(root);

  const count = (dir) => {
    const d = path.join(root, dir);
    if (!fs.existsSync(d)) return 0;
    return fs.readdirSync(d).filter((f) => f.endsWith('.md')).length;
  };

  const latest = (dir, n) => {
    const d = path.join(root, dir);
    if (!fs.existsSync(d)) return [];
    return fs
      .readdirSync(d)
      .filter((f) => f.endsWith('.md'))
      .sort()
      .reverse()
      .slice(0, n);
  };

  const now = new Date();
  const lines = [
    '# SPECTRE INDEX',
    '',
    `*Last updated: ${friendlyDate(now)}*`,
    '',
    '## Vault Statistics',
    `- Analyses: ${count('analyses')}`,
    `- Incidents: ${count('incidents')}`,
    `- Vessel Notes: ${count('vessels')}`,
    `- Daily Summaries: ${count('daily-summaries')}`,
    `- Weekly Summaries: ${count('weekly-summaries')}`,
    '',
    '## Latest Analyses',
    ...latest('analyses', 5).map((f) => `- [[analyses/${f}|${f}]]`),
    '',
    '## Latest Incidents',
    ...latest('incidents', 5).map((f) => `- [[incidents/${f}|${f}]]`),
    '',
  ];

  const fpath = path.join(root, 'SPECTRE-INDEX.md');
  fs.writeFileSync(fpath, lines.join('\n'), 'utf8');
  return fpath;
}

