'use strict';

const fs = require('fs');
const path = require('path');
const config = require('../config');

const VAULT = path.resolve(config.VAULT_PATH);

const DIRS = [
  'analyses',
  'incidents',
  'vessels',
  'daily-summaries',
  'weekly-summaries',
  'patterns',
];

